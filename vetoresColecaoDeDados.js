// Variavel com mais de um valor = coleção de dados = array (vetor)


let nomePessoas = ["Maria", "João", "Pedro", "Ana"]

nomePessoas.pop() // remove o ultimo elemento do array
nomePessoas.push("Carla") // adiciona um elemento no final do array
nomePessoas.shift() // remove o primeiro elemento do array
nomePessoas.unshift("Lucas") // adiciona um elemento no inicio do array

console.log (nomePessoas)

